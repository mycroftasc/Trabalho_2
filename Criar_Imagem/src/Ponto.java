
public class Ponto {

	private int x;
	private int y;

	private int getX() {
		return x;
	}

	private void setX(int x) {
		this.x = x;
	}

	private int getY() {
		return y;
	}

	private void setY(int y) {
		this.y = y;
	}
	
	public Ponto(int vx, int vy){
		
		this.x = vx;
		this.y = vy;
	}
}
